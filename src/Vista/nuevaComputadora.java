    //para que desaparezca el texto Placeholder cuando se da click sobre el textfield
    private void placeHolderIn(javax.swing.JTextField text){
        if(text.getForeground()== Color.GRAY){
            text.setText("");
            text.setForeground(new JTextField().getForeground());
        }
    }
    //para que aparezca el texto Placeholder correspondiente cuando se da click fuera del textfield
    private void placeHolderOut(javax.swing.JTextField text, String msg){
        if(text.getText().equals("")){
            text.setText(msg);
            text.setForeground(Color.GRAY);
        }
    }
    private void soloNum(javax.swing.JTextField text, java.awt.event.KeyEvent evento){
        Object objeto= evento.getKeyChar();
        String key= objeto.toString();
        try{
            Integer.parseInt(key);
        }
        catch(NumberFormatException e){
            if(evento.getKeyCode() != KeyEvent.VK_BACK_SPACE && 
                    evento.getKeyCode() != KeyEvent.VK_LEFT && 
                    evento.getKeyCode() != KeyEvent.VK_RIGHT && 
                    evento.getKeyCode() != KeyEvent.VK_SHIFT){
                String nuevaCad= "";
                char[] cadActual= text.getText().toCharArray();
                for(int i= 0; i < cadActual.length - 1; i++)
                    nuevaCad+= cadActual[i];
                text.setText(nuevaCad);
            }
        }
    }
    
    private void jComboBox1ItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_jComboBox1ItemStateChanged
        Object item= jComboBox1.getModel().getSelectedItem();
        Object[] items;
        if(item.equals("Windows XP"))items= new Object[]{"Home","Professional","Media Center","64 bit"};
        else if(item.equals("Windows 7")) items= new Object[]{"Starter","Home basic","Home Premium","Professional","Ultimate","Enterpise"};
        else if(item.equals("Windows 8.1"))items= new Object[]{"Core","Pro","Enterprise"};
        else if(item.equals("Windows 10"))items= new Object[]{"Home","Pro","Enterprise","Education"};
        else if(item.equals("Windows 11"))items= new Object[]{"Home","Pro","Enterprise","Education"};
        else if(item.equals("Linux"))items= new Object[]{"Ubuntu","Fedora","Debian","Arch linux"};
        else return;
        ComboBoxModel model= new DefaultComboBoxModel(items);
        jComboBox2.setModel(model);
    }//GEN-LAST:event_jComboBox1ItemStateChanged

