    private List<Component> getHermanos(JLabel objetivo){
        List<Component> hermanos= new LinkedList<>();
        Container padre= objetivo.getParent();
        for(Component hijo : padre.getComponents()){
            if(hijo instanceof JLabel)
                continue;
            hermanos.add(hijo);
        }
        return hermanos;
    }
    
    private void getLabels(Container inicio, List<JLabel> labels){
        for(Component elem : inicio.getComponents()){
            if(elem instanceof JLabel label && !label.getText().equals("."))
                labels.add(label);
            else if(elem instanceof JPanel contenedor) getLabels(contenedor, labels);
        }
    }
    
    //acción del botón "Guardar"
    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        Map<String,List<Object>> equipoNew= new HashMap<>();
        Boolean vacios= false;
        Boolean datosIncompletos= false;
        List<JLabel> labels= new LinkedList<>();  //conteandrá las claves referentes a los atributos del equipo informático
        getLabels(jPanel1, labels);
        // <editor-fold defaultstate="collapsed" desc="Recopilar información y validación"> 
        for(JLabel etiqueta : labels){
            List<Object> hermanos= new LinkedList<>(); //contendrá los valores que se guardarán por clave
            String clave= etiqueta.getText().split(":")[0];  //para quitar los 2 puntos de la clave
            clave= clave.equals("Monitor")? "Monitor 1" : clave; //cambio "Monitor" por "Monitor 1" si fuera el caso
            clave= clave.equals("Disco Duro")? "Disco Duro 1" : clave; //cambio "Disco Duro" por "Disco Duro 1" si fuera el caso
            Integer cantValidadosOK= 0;  //solo se guardarán las claves que tengan todos los valores validados
            for(Component control : getHermanos(etiqueta)){
                Object valor= "";
                if(control instanceof JTextField textField){
                    valor= textField.getText();
                    if(!valor.equals("") && !valor.equals("Nombre") && !valor.equals("Cargo") && 
                            !valor.equals("Versión") && !valor.equals("Marca") && !valor.equals("Modelo") && 
                            !valor.equals("No. Serie") && !valor.equals("No. Inventario")){
                        textField.setBorder(new JTextField().getBorder());
                        cantValidadosOK++;
                    }
                    else if(!clave.equals("Lector CD/DVD") && !clave.equals("Lector 3 1/2") && 
                            !clave.equals("UPS") && !clave.equals("Impresora") && !clave.equals("Scanner") && 
                            !clave.equals("Fotocopiadora")){
                        textField.setBorder(new LineBorder(Color.RED, 2));
                        //esta asignacion es superimportante
                        //si hay algun campo vacio no permite el paso de la informacion al controlador
                        vacios= true;
                    }
                }
                else if(control instanceof JComboBox comboBox){
                    if(comboBox.isEditable()) valor= comboBox.getEditor().getItem();
                    else valor= comboBox.getSelectedItem();
                    if(!valor.equals("")){
                        comboBox.setBorder(new JComboBox<>().getBorder());
                        cantValidadosOK++;
                    }
                    else if(!clave.equals("Lector CD/DVD") && !clave.equals("Lector 3 1/2") &&
                            !clave.equals("UPS") && !clave.equals("Impresora") && !clave.equals("Scanner") &&
                            !clave.equals("Fotocopiadora")){
                        comboBox.setBorder(new LineBorder(Color.RED, 2));
                        //lo mismo que arriba
                        vacios= true;
                    }
                }
                else if(control instanceof JSpinner spinner){
                    valor= spinner.getValue();
                    cantValidadosOK++;
                }
                hermanos.add(valor);
            }
            /*agrega cada componente y propiedad que tenga sus datos completos 
            al equipo nuevo que se va a registrar*/
            if(cantValidadosOK.equals(hermanos.size())) equipoNew.put(clave, hermanos);
            /*si hay algun dispositivo o componente con datos incompletos mostrará una advertencia,
            esto es para los dispositivos o periféricos prescindibles*/
            else if(cantValidadosOK != 0) datosIncompletos= true;
            //agrega el id en null a cada dispositivo o periférico agregado
            if(equipoNew.get(clave) != null && !clave.equals("Nombre del equipo (RED)") 
                    && !clave.equals("Tipo") && !clave.equals("IP") && !clave.equals("Departamento") 
                    && !clave.equals("No. Sello") && !clave.equals("No. Llavero") && !clave.equals("Estado"))
                equipoNew.get(clave).add(null);
        }
        //</editor-fold>
        //como dije antes si esto se cumple no se permite el paso de la informacion al controlador
        if(vacios){
            JOptionPane.showMessageDialog(this, "Hay campos vacios que son obligatorios\n " + 
                    "Mire los campos con bordes en rojo", "Advertencia", JOptionPane.WARNING_MESSAGE);
            return;
        }
        if(datosIncompletos){
            JOptionPane.showMessageDialog(this, "Hay dispositivos prescindibles con datos incompletos\n " + 
                    "No se guardarán en la BD si no se completan", "Advertencia", JOptionPane.WARNING_MESSAGE);
            return;
        }
        //a partir de aqui se hace el envio de los datos hacia el controlador
        gestionEquipo controlador= new gestionEquipo();
        String estado= controlador.registrarComputadora(equipoNew, "agregar");
        JOptionPane.showMessageDialog(this, estado, "Información", JOptionPane.INFORMATION_MESSAGE);
        if(estado.equals("El equipo se registró correctamente en la BD")){
            String nombreAdmin= ((Vista.Inicio)this.getOwner()).getNombreAdmin();
            Controlador.gestionEventos.agregarEvento("Responsable: " + nombreAdmin + "\n" + 
                    "Lugar: Sistema\n" + "El administrador agregó una nueva computadora " + 
                    "con el nombre " + equipoNew.get("Nombre del equipo (RED)").getFirst());
            this.dispose();
        }
    }//GEN-LAST:event_jButton1ActionPerformed

