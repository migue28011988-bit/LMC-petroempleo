    private void jComboBox6ItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_jComboBox6ItemStateChanged
        String tema= OpcionesConfig.cargarTema();
        List<Object> tipos= new ArrayList<>(List.of("PC", "Servidor"));
        if(tipos.contains(jComboBox6.getSelectedItem())){
            if(tema.contains("Dar") || tema.contains("HiFi") || tema.contains("Noire"))
                jLabel27.setIcon(new ImageIcon(getClass().getResource("/Iconos/PC_b.png")));
            else jLabel27.setIcon(new ImageIcon(getClass().getResource("/Iconos/PC.png")));
        }
        else {
            if(tema.contains("Dar") || tema.contains("HiFi") || tema.contains("Noire"))
                jLabel27.setIcon(new ImageIcon(getClass().getResource("/Iconos/laptop_b.png")));
            else jLabel27.setIcon(new ImageIcon(getClass().getResource("/Iconos/laptop.png")));
        }
    }//GEN-LAST:event_jComboBox6ItemStateChanged

