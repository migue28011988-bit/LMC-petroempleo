        sorter= new TableRowSorter<>(jTable1.getModel());
        jTable1.setRowSorter(sorter);
        /*establece un comparador personalizado para la columna del tamaño ya que el sorter
        por defecto asignado no puede hacer casting a float ya que esa columna tiene como tipo double*/
        comparApps= new Comparator<Object>() {
            @Override
            public int compare(Object o1, Object o2) {
                String cad1= o1.toString();
                String cad2= o2.toString();
                Double op1= Double.valueOf(cad1);
                Double op2= Double.valueOf(cad2);
                if(op1 < op2) return -1;
                else if (op1 > op2) return 1;
                return 0; 
            }
        };
        sorter.setComparator(4, comparApps);
        //no ordena la columna de la versión
        sorter.setSortable(2, false);
        administrarApps(null, "asignar");
        
        //para listar todos los cambios registrados del equipo
        cambiarIconoNombreEquipo(jLabel17);
        //establece un sorter automático
        jTable2.setAutoCreateRowSorter(true);
