    //acción del  botón "Actualizar" en la ficha técnica
    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        if(jToggleButton1.isSelected()){
            String antiguaIP= equipo.get("IP").get(0).toString() + "." + equipo.get("IP").get(1).toString() + "." +
                    equipo.get("IP").get(2).toString() + "." + equipo.get("IP").get(3).toString();
            AtomicBoolean vacios= new AtomicBoolean(false);
            String estado;
            administrarFicha(jPanel3, new JLabel(), new AtomicInteger(0), vacios, "actualizar");
            if(vacios.get() == true){
                JOptionPane.showMessageDialog(this, "No se admiten campos vacios" +
                        " o datos incorrectos \n Revise los campos en rojo", "Advertencia", JOptionPane.WARNING_MESSAGE);
                return;
            }
            String actualIP= equipo.get("IP").get(0).toString() + "." + equipo.get("IP").get(1).toString() + "." +
                    equipo.get("IP").get(2).toString() + "." + equipo.get("IP").get(3).toString();
            if(!antiguaIP.equals(actualIP)) equipo.put("antiguaIP", List.of(antiguaIP));
            estado= controlador.registrarComputadora(equipo, "actualizar");
            JOptionPane.showMessageDialog(this, estado, "Estado", JOptionPane.INFORMATION_MESSAGE);
            if(estado.equals("Equipo actualizado") && controlador.getCambios()){
                String nombreAdmin= ((Vista.Inicio)this.getOwner()).getNombreAdmin();
                gestionEventos.agregarEvento("Responsable: " + nombreAdmin + "\n" + 
                        "Lugar: Sistema\n" + "El administrador realizó cambios en la ficha técnica " + 
                        "de la computadora " + equipo.get("Nombre del equipo (RED)").getFirst());
                controlador.resetCambios();
            }
            listarCambios();
        }
        else JOptionPane.showMessageDialog(this, "<html>Solo se actualizará si"+
                " la opción <b>Modificar</b> está seleccionada</html>");
    }//GEN-LAST:event_jButton1ActionPerformed

