    private void agregarPerifericoUnico(String clave){
        List<Object> periferico= equipo.get(clave);
        if(periferico != null){
            JOptionPane.showMessageDialog(this, "Ya existe el periférico a agregar!!!", 
                    "Información", JOptionPane.INFORMATION_MESSAGE);
            return;
        }
        if(clave.contains("Lector"))
            periferico= new LinkedList<>(List.of("","",""));
        else
            periferico= new LinkedList<>(List.of("","","",1));
        periferico.add(null);
        equipo.put(clave, periferico);
        administrarFicha(jPanel3, new JLabel(), new AtomicInteger(0), null, "asignar");
        if(jToggleButton1.isSelected())
            administrarFicha(jPanel3, new JLabel(), new AtomicInteger(0), null, "modificar si");
        else administrarFicha(jPanel3, new JLabel(), new AtomicInteger(0), null, "modificar no");
    }
    
