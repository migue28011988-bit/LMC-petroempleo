    //acción del botón "Eliminar" del registro de cambios, el cual elimina un cambio escogido
    private void jButton13ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton13ActionPerformed
        DefaultTableModel tabla= (DefaultTableModel)jTable2.getModel();
        int fila= jTable2.getSelectedRow();
        if(fila == -1){
            JOptionPane.showMessageDialog(this, "No se ha seleccionado un cambio", "Información",
                    JOptionPane.INFORMATION_MESSAGE);
            return;
        }
        fila= jTable2.convertRowIndexToModel(fila);
        List<Object> cambio= new LinkedList<>();
        for(int column= 0; column < 5; column++)
            cambio.add(tabla.getValueAt(fila, column));
        Iterator<Map.Entry<String, List<Object>>> it= equipo.entrySet().iterator();
        while(it.hasNext()){
            Map.Entry<String, List<Object>> entrada= it.next();
            if(entrada.getKey().contains("cambio") && entrada.getValue().equals(cambio)){
                it.remove();
                break;
            }
        }
        String estado= controlador.registrarComputadora(equipo, "actualizar");
        if(estado.equals("Equipo actualizado")){
            JOptionPane.showMessageDialog(this, "El cambio escogido fue eliminado de la BD", 
                    "Información", JOptionPane.INFORMATION_MESSAGE);
            String nombreAdmin= ((Vista.Inicio)this.getOwner()).getNombreAdmin();
            gestionEventos.agregarEvento("Responsable: " + nombreAdmin + "\n" + 
                    "Lugar: Sistema\n" + "El administrador eliminó un cambio del registro de cambios " + 
                    "de la computadora con el nombre " + equipo.get("Nombre del equipo (RED)").getFirst());
        }
        listarCambios();
    }//GEN-LAST:event_jButton13ActionPerformed

