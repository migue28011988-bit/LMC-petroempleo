    //acción del botón "Guardar" el cuál valida y registra el equipo en la base de datos
    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        //<editor-fold defaultstate="collapsed" desc="Validación">
        boolean vacios= false;
        JFormattedTextField formatField= new JFormattedTextField();
        JTextField textField= new JTextField();
        
        if(jFormattedTextField1.getText().contains(" ")){
            jFormattedTextField1.setBorder(new LineBorder(Color.red));
            vacios= true;
        }
        else jFormattedTextField1.setBorder(formatField.getBorder());
        
        Queue<JTextField> cola= new LinkedList<>(List.of(jTextField1, jTextField2, jTextField3, jTextField4, jTextField5, jTextField6));
        for(int i= 0; i < 6; i++){
            JTextField ref= cola.poll();
            if(ref.getText().equals("") || ref.getText().equals("Nombre") ||
                    ref.getText().equals("Cargo") || ref.getText().equals("Marca")||
                    ref.getText().equals("Modelo") || ref.getText().equals("No. Serie")){
                ref.setBorder(new LineBorder(Color.red));
                vacios= true;
            }
            else ref.setBorder(textField.getBorder());
        }
        
        if(vacios == true){
            JOptionPane.showMessageDialog(this, "Hay campos incompletos o vacios\nRevise los campos en rojo",
                    "Advertencia", JOptionPane.WARNING_MESSAGE);
            return;
        }
        //</editor-fold>
        
        //<editor-fold defaultstate="collapsed" desc="Registro">
        Map<String, String> equipoNew= new HashMap();
        if(!ID.equals(-1))
            equipoNew.put("ID", ID.toString());
        equipoNew.put("Código", jFormattedTextField1.getText());
        String ip= jSpinner2.getValue().toString() + "." + jSpinner3.getValue().toString() + "." +
                jSpinner4.getValue().toString() + "." + jSpinner5.getValue().toString();
        equipoNew.put("IP", ip);
        equipoNew.put("NumInventario", jSpinner1.getValue().toString());
        equipoNew.put("Departamento", jTextField3.getText());
        equipoNew.put("NombreUsuario", jTextField1.getText());
        equipoNew.put("CargoUsuario", jTextField2.getText());
        equipoNew.put("Marca", jTextField4.getText());
        equipoNew.put("Modelo", jTextField5.getText());
        equipoNew.put("NumSerie", jTextField6.getText());
        equipoNew.put("CódigoAntiguo", codigo.toString());
        String estado= controladorEq.registrarVOIP(equipoNew);
        JOptionPane.showMessageDialog(this, estado, "Información", JOptionPane.INFORMATION_MESSAGE);
        if(estado.equals("Equipo correctamente registrado en la Base de datos") ||
                estado.equals("Equipo actualizado")){
            String nombreAdmin= ((Vista.Inicio)this.getOwner()).getNombreAdmin();
            if(estado.contains("registrado"))
                Controlador.gestionEventos.agregarEvento("Responsable: " + nombreAdmin + "\n" + 
                        "Lugar: Sistema\n" + "El administrador agregó un nuevo teléfono VOIP " + 
                        "con el código " + equipoNew.get("Código"));
            else if(controladorEq.getCambios())
                Controlador.gestionEventos.agregarEvento("Responsable: " + nombreAdmin + "\n" + 
                        "Lugar: Sistema\n" + "El administrador modificó los datos del teléfono VOIP " + 
                        "con el código " + equipoNew.get("Código"));
            this.dispose();
        }
        //</editor-fold>
    }//GEN-LAST:event_jButton1ActionPerformed

