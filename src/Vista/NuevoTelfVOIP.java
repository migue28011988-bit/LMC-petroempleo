    private void asignarValores(){
        Map<String, String> voip= controladorEq.obtenerVOIP(codigo);
        if( voip == null) return;
        ID= Integer.valueOf(voip.get("ID"));
        JTextField text= new JTextField();
        jFormattedTextField1.setText(voip.get("Código"));
        String[] ip= voip.get("IP").split("\\.");
        jSpinner2.setValue(Integer.valueOf(ip[0]));
        jSpinner3.setValue(Integer.valueOf(ip[1]));
        jSpinner4.setValue(Integer.valueOf(ip[2]));
        jSpinner5.setValue(Integer.valueOf(ip[3]));
        jSpinner1.setValue(Integer.valueOf(voip.get("NumInventario")));
        jTextField3.setText(voip.get("Departamento"));
        jTextField1.setText(voip.get("NombreUsuario"));
        jTextField1.setForeground(text.getForeground());
        jTextField2.setText(voip.get("CargoUsuario"));
        jTextField2.setForeground(text.getForeground());
        jTextField4.setText(voip.get("Marca"));
        jTextField4.setForeground(text.getForeground());
        jTextField5.setText(voip.get("Modelo"));
        jTextField5.setForeground(text.getForeground());
        jTextField6.setText(voip.get("NumSerie"));
        jTextField6.setForeground(text.getForeground());
    }
    
