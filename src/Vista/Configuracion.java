    private void adminCuentas(String operacion){
        DefaultTableModel tabla= (DefaultTableModel)jTable1.getModel();
        //<editor-fold defaultstate="collapsed" desc="las operaciones de la sección cuentas">
        if(operacion.equals("asignar")){
            controladorConf.adminCuentas(OpcionesConfig.GET_CUENTAS, null);
            for(Object[] cuenta : controladorConf.getCuentas()){
                Object[] rowData= new Object[]{cuenta[1], cuenta[2], cuenta[3]};
                tabla.addRow(rowData);
                //oculta las contraseñas en la tabla de las cuentas
                int lon= cuenta[2].toString().length();
                String password= "";
                for(int i= 0; i < lon; i++) password+= '\u2022';
                tabla.setValueAt(password, tabla.getRowCount() - 1, 1);
            }
            //esto es para el tamaño de las columnas
            jTable1.getColumnModel().getColumn(0).setPreferredWidth(jTable1.getParent().getWidth() - 290);
            jTable1.getColumnModel().getColumn(1).setPreferredWidth(200);
            jTable1.getColumnModel().getColumn(2).setPreferredWidth(90);
            //esto es para cuando se de click sobre el checkbox de la columna SuperAdmin.
            jTable1.addMouseListener(new MouseAdapter() {
                @Override
                public void mousePressed(MouseEvent e) {
                    //<editor-fold defaultstate="collapsed" desc="Código">
                    super.mousePressed(e);
                    Point coorMouse= e.getPoint();
                    for(int row= 0; row < jTable1.getRowCount(); row++){
                        int column= 0;
                        for(int col= 0; col < 3; col++)
                            if(jTable1.getColumnName(col).equals("SuperAdmin."))
                                column= col;
                        int coordX= jTable1.getCellRect(row, column, false).x + 40;
                        int coordY= jTable1.getCellRect(row, column, false).y + 5;
                        if(coorMouse.x > coordX && coorMouse.x < (coordX + 10) 
                                && coorMouse.y > coordY && coorMouse.y < (coordY + 10)){
                            boolean valor= (boolean)jTable1.getValueAt(row, column);
                            valor= !(valor == true);
                            //con la siguiente condición se garantiza que exista al menos 1 superadministrador
                            if(!valor){
                                int cantSuper= 0;
                                for(int i= 0; i < jTable1.getRowCount(); i++)
                                    if((boolean)jTable1.getValueAt(i, column))
                                        cantSuper+= 1;
                                if(cantSuper == 1){
                                    JOptionPane.showMessageDialog(null, "Debe de existir al menos un Superadministrador",
                                            "Advertencia", JOptionPane.WARNING_MESSAGE);
                                    return;
                                }
                            }
                            //agrega el valor de superAdmin a la tabla
                            jTable1.setValueAt(valor, row, column);
                            /*actualiza la cuenta en la lista de cuentas en el controlador, algo parecido a lo que se hace
                            con "Cambiar credenciales"*/
                            String AdminName= tabla.getValueAt(row, 0).toString();
                            String password= controladorConf.getPasswordDe(AdminName);
                            boolean superAdmin= valor;
                            controladorConf.adminCuentas(OpcionesConfig.UPDATE_CUENTA, 
                                    new Object[]{AdminName, AdminName, password, superAdmin});
                            break;
                        }
                    }
                    //</editor-fold>
                }
            });
        }
        else if(operacion.equals("añadir")){
            jTextField1.setText("");
            jPasswordField1.setText("");
            jButton7.setText("Añadir");
            jButton7.setIcon(new ImageIcon(getClass().getResource("/Iconos/plus.png")));
            nuevaCuenta.setTitle("Nueva Cuenta");
            mostrarVentanaDeCredenciales();
        }
        else if(operacion.equals("eliminar")){
            int fila= jTable1.getSelectedRow();
            if(fila == -1){
                JOptionPane.showMessageDialog(this, "Debe seleccionar una cuenta a eliminar"
                        , "Eliminar cuenta", JOptionPane.INFORMATION_MESSAGE);
                return;
            }
            Object AdminName= tabla.getValueAt(fila, 0);
            controladorConf.adminCuentas(OpcionesConfig.ERASE_CUENTA, new Object[]{AdminName});
            tabla.removeRow(fila);
            mostrarMSG(this, "Cuenta eliminada");
        }
        else if(operacion.equals("credenciales")){
            int fila= jTable1.getSelectedRow();
            if(fila == -1){
                JOptionPane.showMessageDialog(this, "Debe seleccionar una cuenta para cambiar sus credenciales"
                        , "Cambiar credenciales de la cuenta", JOptionPane.INFORMATION_MESSAGE);
                return;
            }
            String AdminName= tabla.getValueAt(fila, 0).toString();
            jTextField1.setText(AdminName);
            jPasswordField1.setText(controladorConf.getPasswordDe(AdminName));
            jButton7.setText("Cambiar");
            jButton7.setIcon(new ImageIcon(getClass().getResource("/Iconos/actualización.png")));
            nuevaCuenta.setTitle("Cambiar Credenciales");
            mostrarVentanaDeCredenciales();
        }
        else if(operacion.equals("actualizar")){
            /*aunque se creen cuentas nuevas, se modifiquen las credenciales o se eliminen cuentas existentes,
            realmente lo que se hace es realizar esas operaciones a una lista de cuentas en el controlador,
            no se realizan en la base de datos, para que los cambios tengan efecto en la BD,
            se deben aplicar los cambios con el botón "Actualizar cambios"*/
            controladorConf.adminCuentas(OpcionesConfig.UPDATE_CUENTAS, null);
            if(controladorConf.getCambios()){
                String nombreSuperAdmin= ((Vista.Inicio)this.getOwner()).getNombreAdmin();
                gestionEventos.agregarEvento("Responsable: " + nombreSuperAdmin + "\n" + 
                        "Lugar: Sistema\n" + "El superadministrador realizó cambios en las cuentas");
                controladorConf.resetCambios();
            }
            mostrarMSG(this, "Los cambios realizados se han actualizado en la Base de datos");
        }
        //</editor-fold>
    }
    
