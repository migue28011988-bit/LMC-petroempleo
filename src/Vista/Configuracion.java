    //acción del botón "Aplicar" en la pestaña de la Apariencia para aplicar un tema a la app
    private void jButton8ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton8ActionPerformed
        LF= jList1.getSelectedValue();
        if(LF == null){
            JOptionPane.showMessageDialog(this, "Debe selecionar un Tema para cambiarlo",
                    "Información", JOptionPane.INFORMATION_MESSAGE);
            return;
        }
        if(controladorConf.getTema().equals(LF)) return;
        JOptionPane.showMessageDialog(this, "<html>La aplicación debe reiniciarse para aplicar la nueva <b>Apariencia</b></html>",
                "Información", JOptionPane.INFORMATION_MESSAGE);
        controladorConf.aplicarTema(LF);
        cambioTema= true;
        String nombreSuperAdmin= ((Vista.Inicio)this.getOwner()).getNombreAdmin();
        gestionEventos.agregarEvento("Responsable: " + nombreSuperAdmin + "\n" + 
                "Lugar: Sistema\n" + "El superadministrador cambió el tema de apariencia del sistema a " + 
                LF);
        this.dispose();
    }//GEN-LAST:event_jButton8ActionPerformed

